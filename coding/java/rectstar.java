public class rectstar {
    public static void main(String args[]) {
        int i, j, n, m;
        n = 4;
        m = 5;
        for (i = 1; i <= n; i++) {
            for (j = 1; j <= m; j++) {
                System.out.print("*");
            }
            System.out.print("\n");
        }
    }
}
